/// Represents a language.
#[derive(Default, Clone, Debug)]
pub enum Language {
    /// English language
    #[default]
    English,
    /// Russian language
    Russian,
    /// Portuguese language
    Portuguese,
    /// Ukrainian language
    Ukrainian,
    /// Deutsch language
    Deutsch,
    /// Italian language
    Italian,
    /// Polish language
    Polish,
    /// French language
    French,
}
